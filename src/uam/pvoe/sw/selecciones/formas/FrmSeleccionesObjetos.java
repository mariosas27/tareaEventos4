    public void mostrarListaAlumnos(){
        
        Licenciatura licenciatura = (Licenciatura) cmbListaLicenciaturas.getSelectedItem(); 
//        System.out.println(licenciatura.getNombre());
//        System.out.println(licenciatura.getClave());
        
        int indice = cmbListaLicenciaturas.getSelectedIndex(); 
        if(indice != 0){
            lblAlumno.setVisible(true);
            cmbListaAlumnos.removeAllItems();
            llenarListaAlumnos(licenciatura.getClave());
            cmbListaAlumnos.setVisible(true); 
            btnMerece.setVisible(true); 
            btnNoMerece.setVisible(true); 
            btnRegistrar.setVisible(true); 
            
        }else{
           limpiarCampos(); 
           ocultarTodoElementos();
        }
        
    }
    
    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        //mostramos la clave de la licenciatura
        System.out.println("----------------------------------- ALUMNO REGISTRADO ----------------------------------------");
        Licenciatura lic = (Licenciatura)cmbListaLicenciaturas.getSelectedItem(); 
        System.out.println("CLAVE LICENCIATURA: " + lic.getClave());
        Alumno alumno = (Alumno)cmbListaAlumnos.getSelectedItem(); 
        System.out.println("MATRICULA DEL ALUMNO: " + alumno.getMatricula());
        
        //PARA REVISAR SI QUIERE APOYO O NO 
        Enumeration<AbstractButton> btnGrp = btnGrpApoyo.getElements(); 
        while(btnGrp.hasMoreElements()){
            JRadioButton btnAux = (JRadioButton) btnGrp.nextElement(); 
            if(btnAux.isSelected()){
                Opcion resultado = (Opcion) btnAux.getClientProperty("key");
                System.out.println("EVALUACION DE APOYO:  " + resultado.getValor());
                if(resultado.getLlave().equals("MERECE")){
                    System.out.println("APOYOS:");
                    for(int i = 0; i < listaChkBoxes.size(); ++i){
                        JCheckBox aux = listaChkBoxes.get(i); 
                        if(aux.isSelected()){
                            Opcion opcion = (Opcion) aux.getClientProperty("llave"); 
                            System.out.println(opcion.getLlave());
                        }
                    }
                }
            }
        }
        
        
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

